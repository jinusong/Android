package com.example.dsm2018.recyclerview_practice;

public class Item {
    public String name;
    public int number;

    public String getname() {
        return name;
    }

    public int getnumber() {
        return number;
    }

    public Item(String name, int number) {
        this.name = name;
        this.number = number;
    }
}
