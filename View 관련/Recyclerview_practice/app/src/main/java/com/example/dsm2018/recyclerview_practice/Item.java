package com.example.dsm2018.recyclerview_practice;

public class Item {
    public String name;
    public String number;

    public String getname() {
        return name;
    }

    public String getnumber() {
        return number;
    }

    public Item(String name, String number) {
        this.name = name;
        this.number = number;
    }
}
